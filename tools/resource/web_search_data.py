# Functions to get all queries
def get_all_queries():
    """Get all query list"""
    all_queries = []
    for task_id, task_data in TASK_DATA.items():
        if "calls" in task_data:
            for call in task_data["calls"]:
                all_queries.extend(call.get("queries", []))
        else:
            all_queries.extend(task_data.get("queries", []))
    return all_queries

# Get result by task_id
def get_result_by_task_id(task_id: str):
    """Get search results by task_id"""
    task_data = TASK_DATA.get(task_id)
    if task_data:
        # Check if multi-call format
        if "calls" in task_data:
            # For multi-call, can return single call result or aggregated result
            # For simplicity, return single call result here
            return task_data["calls"][0].get("result")
        return task_data.get("result")
    return None

# Get task statistics
def get_stats():
    """Get data statistics"""
    total_queries = 0
    total_tasks = len(TASK_DATA)
    for task_id, task_data in TASK_DATA.items():
        if "calls" in task_data:
            for call in task_data["calls"]:
                total_queries += len(call.get("queries", []))
        else:
            total_queries += len(task_data.get("queries", []))
            
    return {
        "total_tasks": total_tasks,
        "total_queries": total_queries,
        "avg_queries_per_task": total_queries / total_tasks if total_tasks > 0 else 0
    }

